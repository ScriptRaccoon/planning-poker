<h1>Planning Poker</h1>

<p>
	You can use this app to play <i>Planning Poker</i> in your scrum team.
</p>

<p>
	The rules are simple: Within a <a href="/room">room</a>, every
	member has to give an estimate for a particular user story. At
	first, these estimates are not revealed. When everyone has given
	an estimate, the moderator can reveal the estimates. If the
	estimates are unequal, the team tries to find a consensus.
</p>
