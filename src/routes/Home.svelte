<h1>Planning Poker</h1>

<p>
	You can use this app to play <i>Planning Poker</i> in your scrum team.
</p>

<h2>Rules</h2>

<ul>
	<li>
		Within a <a href="/room">room</a>, every member has to give an
		estimate for a particular user story.
	</li>
	<li>At first, these estimates are not revealed.</li>
	<li>
		When everyone has given an estimate, the moderator can reveal
		the estimates.
	</li>
	<li>
		If the estimates are unequal, the team tries to find a
		consensus.
	</li>
</ul>

